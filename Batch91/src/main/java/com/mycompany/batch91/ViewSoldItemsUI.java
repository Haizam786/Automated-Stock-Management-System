    private void  viewSoldTable(){
         int x;
        DBCon dbcon=new DBCon();
        Connection con=dbcon.getConnection();
        
        try {
            PreparedStatement stmt= con.prepareStatement("select * from solditems ");
            ResultSet rs=stmt.executeQuery();
            
            ResultSetMetaData rd=rs.getMetaData();
            x=rd.getColumnCount();
            dt=(DefaultTableModel)jTable1.getModel();
            dt.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i=1; i<=x; i++);{
                
                v2.add(rs.getString("no"));
                v2.add(rs.getString("salesNo"));
                v2.add(rs.getString("itemId"));
                v2.add(rs.getString("price"));
                v2.add(rs.getString("qty"));
                 v2.add(rs.getString("total"));
                               
            }
                dt.addRow(v2);
                
                
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewSoldItemsUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
